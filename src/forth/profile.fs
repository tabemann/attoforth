NEW-LOCK CONSTANT PROFILE-LOCK
VARIABLE LAST-PROFILE-WORD 0 LAST-PROFILE-WORD !

: UPDATE-LAST-PROFILE-WORD ( addr -- addr )
  PROFILE-LOCK LOCK LAST-PROFILE-WORD @ SWAP LAST-PROFILE-WORD !
  PROFILE-LOCK UNLOCK ;

BEGIN-STRUCTURE PROFILE-DATA-SIZE
  FIELD: PROFILE-XT
  FIELD: PROFILE-NEXT
  FIELD: PROFILE-COUNT
END-STRUCTURE

: PROFILE. ( -- )
  LAST-PROFILE-WORD @
  BEGIN DUP 0<> WHILE
    CR DUP PROFILE-XT @ DUP NAME>STRING TYPE SPACE 16 BASE.
    DUP PROFILE-COUNT @ 10 BASE.
    PROFILE-NEXT @
  REPEAT
  DROP ;

: RESET-PROFILE ( -- )
  LAST-PROFILE-WORD @
  BEGIN DUP 0<> WHILE
    DUP 0 SWAP PROFILE-COUNT !
    PROFILE-NEXT @
  REPEAT
  DROP ;

