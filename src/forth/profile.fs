  & BRANCH HERE [ PROFILE-DATA-SIZE 2 CELLS + ] LITERAL + ,
  0 , HERE UPDATE-LAST-PROFILE-WORD LATESTXT , , HERE 0 ,
  & (LITERAL) 1 , & (LITERAL) , & +! ;

: :NONAME ( -- xt ) :NONAME COMPILE-PROFILE ;

: : ( "name" -- ) : COMPILE-PROFILE ;

